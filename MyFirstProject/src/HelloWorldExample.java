
public class HelloWorldExample {

	public static void main(String[] args) {
		byte myByte = 25;			// byte
		short myShort = 5000;		// short
		int myInt = 65000;			// integer
		long myLong = 1000000;		// long
		float myFloat = 10.034f;	// float
		double myDouble = 3.14757;	// double
		boolean myBoolean = true;	// boolean
		char myChar = 'm';			// char
		System.out.println("Byte " + myByte + " Short " + myShort + " Integer " + myInt +
				" Long " + myLong + " Float " + myFloat + " Double " + myDouble + " Boolean "
				+ myBoolean + " Char " + myChar);
	}

}
